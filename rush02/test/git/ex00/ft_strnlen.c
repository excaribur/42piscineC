int	ft_strnlen(char *str, char c)
{
	int	count;
	int	i;

	i = 0;
	count = 0;
	while (str[i] != c && str[i] != 0)
	{
		if (str[i])
			return (0);
		if (str[i] != ' ')
			count++;
		i++;
	}
	return (count);
}
