#include <unistd.h>

void	print(int i)
{
	char	c;

	if (i == 0)
	{
		return ;
	}
	c = '0' + i % 10;
	i = i / 10;
	print(i);
	write(1, &c, 1);
}

void	ft_putnbr(int a)
{
	if (a <= -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (a > 0)
	{
		print(a);
	}
	else if (a < 0)
	{
		a = -a;
		write(1, "-", 1);
		print(a);
	}
	else
	{
		write(1, "0", 1);
	}
}
