int	ft_check_argerror(int **clue)
{
	int	i;
	int	a;

	i = 0;
	while (i < 4)
	{
		a = clue[0][i] + clue[3][i];
		if (a >= 6)
			return (0);
		i++;
	}
	i = 0;
	a = 0;
	while (i < 4)
	{
		a = clue[i][0] + clue[i][3];
		if (a >= 6)
			return (0);
		i++;
	}
}
